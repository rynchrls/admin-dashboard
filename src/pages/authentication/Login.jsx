function CustomLink({ to, children, ...props }) {
  const resolvedPath = useResolvedPath(to);
  const isActive = useMatch({ path: resolvedPath.pathname });
  return (
    <button className={isActive ? "current" : ""}>
      <Link to={to} {...props}>
        {children}
      </Link>
    </button>
  );
}

